let num = 50;
switch(num){
    case num<49:
        console.log("false");
        break;
    case num>100:
        console.log("Too lot");
        break;
    case num>80:
        console.log("Almost too lot");
        break;
    case 50:
        console.log("True");
        break;    
    default:
        console.log("Something gone wrong");
        break;           
}